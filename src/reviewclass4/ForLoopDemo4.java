package reviewclass4;

public class ForLoopDemo4 {
    public ForLoopDemo4() {
    }

    public static void main(String[] args) {
        int x = 0;
        System.out.println("1");

        while(x < 10) {
            System.out.println("4");
            ++x;
            System.out.println("3");
        }

    }
}
